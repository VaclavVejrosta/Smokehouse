		  DS18B20_Write (CurrentPort, CurrentPin, 0xBE);  // Read Scratch-pad

		  Temp_byte1 = DS18B20_Read(CurrentPort, CurrentPin);
		  Temp_byte2 = DS18B20_Read(CurrentPort, CurrentPin);

